fn main() {
    let pair = (1,1);
    // 试一试将不同的值赋给'pair'

    println!("告诉我一个数{:?}",pair);
    // match可以解构一个元组
    match pair {
        // 解构出第二个值
        (0,y) => println!("第一个是'0',第二个y的值是'{:?}'",y),
        (x,0) => println!("'x'的值是'{:?}',第二个值的是0",x),
        _ => println!("值是什么并不重要"),
        // '_'  表示不将值绑定到变量
    }
}